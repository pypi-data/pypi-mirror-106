class EncapsulacionEnClase:
    """La lógica de los scripts debe de encapsularse en clases.
        Este es un ejemplo de documentación en Sphinx.

        :param parametro1: parametro de muestra de tipo lista.
        :type parametro1: list
        :param parametro2: parametro de muestra de tipo cadena.
        :type parametro1: str
        """
    def __init__(self, parametro1, parametro2="prueba"):
        self.parametro1 = parametro1
        self.parametro2 = parametro2
